# Programa de lambda que suma dos números.
suma = lambda a, b: a + b
# El programa usa la funcion lambda para sumar dos números.
# La función lambda recibe dos argumentos y retorna la suma de ambos.
resultado = suma(10, 5)
print(f"La suma es: {resultado}")

# Que es la funcion lambda en general
# lambda argumentos: expresion
# la funcion lambda es una forma de definir funciones de manera más compacta
# y sin necesidad de asignar un nombre a la función.
